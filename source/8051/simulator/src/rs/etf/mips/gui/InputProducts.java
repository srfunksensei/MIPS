    private boolean isNumber(String sting_number) {
        for (int i = 0; i < sting_number.length(); i++) {
            if (sting_number.charAt(i) >= '0' && sting_number.charAt(i) <= '9') {
                continue;
            } else {
                return false;
            }
        }
        return true;
    }
