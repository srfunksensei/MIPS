    private void bAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAddActionPerformed

        // TODO add your handling code here:
        if (tfName.getText().equalsIgnoreCase("") || tfPrice.getText().equalsIgnoreCase("")
                || !isNumber(tfPrice.getText())) {
            if (!isNumber(tfPrice.getText())) {
                if (error == null) {
                    error = new ErrorDialog(null, false);
                }
                error.setVisible(true);
            } else {
                if (warning == null) {
                    warning = new WarningDialog(null, true);
                }
                warning.setVisible(true);
            }
        } else {
            Product p = new Product();
            p.name = tfName.getText();
            p.price = Integer.parseInt(tfPrice.getText());
            parent.addProduct(p);

            tfName.setText("");
            tfPrice.setText("");

            if (!inputed) {
                inputed = true;
            }
        }

    }//GEN-LAST:event_bAddActionPerformed

    private void bFinishActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bFinishActionPerformed
        // TODO add your handling code here:
        if (inputed) {
            parent.setProgOK(true);
            parent.getbProg().setEnabled(false);

            parent.setTime_show(true);
        }
        this.dispose();
}//GEN-LAST:event_bFinishActionPerformed

